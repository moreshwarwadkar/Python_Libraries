# - NumPy : Numerical Python
# - It is used to do Mathematical Operations.

# NumPy is a library used for:

# - Numerical operations
# - Fast array processing
# - Mathematical computations
# - It is faster than Python lists because:
#       :- Uses contiguous memory
#       :- Written in C

#==========================================

# Difference Between List and Array :

# List : It uses Non-Continious Memory Allocation.
# Array : It uses Continious Memory Allocation.

# List : Less Memory Wastage.
# Array : More Memory Wastage.

# List : It can Store Different Kind of Data.
# Array : It can Store Similar Kind of Data.

# List : [1,2,3]+[4,5,6] --> [1,2,3,4,5,6]
# Array : [1,2,3]+[4,5,6] --> [5,7,9]

